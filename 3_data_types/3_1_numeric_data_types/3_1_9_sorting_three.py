"""
Программа, которая упорядочивает три числа от большего к меньшему.
"""

"""
Пример ввода 1:
132
129
135

Пример вывода 1:
135
132
129
---------------
Пример ввода 2:
150
160
156

Пример вывода 2:
160
156
150
"""

# Решение
n1, n2, n3 = int(input()), int(input()), int(input())

min_n = min(n1, n2, n3)
max_n = max(n1, n2, n3)

print(max_n, n1 + n2 + n3 - max_n - min_n, min_n, sep="\n")
